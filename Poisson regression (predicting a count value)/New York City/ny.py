#Context
#The New York City Department of Transportation collects daily data about the number of bicycles
#going over bridges in New York City. This data is used to measure bike utilization as a part of
#transportation planning. This dataset is a daily record of the number of bicycles crossing into
#or out of Manhattan via one of the East River bridges (that is, excluding Bronx thruways and
#the non-bikeable Hudson River tunnels) for a stretch of 9 months.

#Content
#A count of the number of bicycles on each of the bridges in question is provided
#on a day-by-day basis, along with information on maximum and minimum temperature and precipitation.